def parallelogram_area(base, height):
    area = base * height
    return area

# Sample input
base_input = 5
height_input = 3.4

# Calculate the area of the parallelogram
area_output = parallelogram_area(base_input, height_input)

# Display the result
print(f'Base = {base_input}')
print(f'Height = {height_input}')
print(f'Area of the parallelogram = {area_output}')
