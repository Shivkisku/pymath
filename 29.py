import math

# Sample decimal number
decimal_number = 64.0

# Calculate square root
square_root_result = math.sqrt(decimal_number)

# Calculate exponential
exponential_result = math.exp(decimal_number)

# Display the input and the results
print(f"Decimal number = {decimal_number}")
print(f"\nSquare root of {decimal_number} is = {square_root_result}")
print(f"Exponential of {decimal_number} is = {exponential_result}")
