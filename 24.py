# Sample inputs
x_number = 12
y_number = 16

# Create a complex number using the inputs
complex_number = complex(x_number, y_number)

# Display the inputs and the result
print(f"X Number = {x_number}")
print(f"Y Number = {y_number}")

# Print the complex number
print(f"Complex Number = {complex_number}")

# Print the real and imaginary parts
print(f"Real Part = {complex_number.real}")
print(f"Imaginary Part = {complex_number.imag}")
