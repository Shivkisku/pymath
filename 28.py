# Sample decimal numbers
decimal_numbers = [23.45, 67.23, 1.45, 89.67, 0.34]

# Display the input
print(f"Multiple Decimal Points with Comma Separate = {', '.join(map(str, decimal_numbers))}")

# Find the maximum and minimum numbers
maximum_number = max(decimal_numbers)
minimum_number = min(decimal_numbers)

# Display the results
print(f"\nMaximum = {maximum_number}")
print(f"Minimum = {minimum_number}")
